#include "stdafx.h"
#include "ValidationResult.h"

namespace chess
{
	////////////////////////////////////////////////////////////////////////////////////////////////
	bool ValidationResult::IsValid() const
	{
		return m_errors.empty();
	}

	////////////////////////////////////////////////////////////////////////////////////////////////
	void ValidationResult::AddError(ErrorCode error)
	{
		m_errors.push_back(error);
	}

	////////////////////////////////////////////////////////////////////////////////////////////////
	ErrorCodes ValidationResult::PopErrors()
	{
		ErrorCodes result = std::move(m_errors);
		m_errors.clear();
		return result;
	}
}

