#include "pch.h"
#include "View.h"
#include "EventDispatcher.h"

namespace mvc
{
	////////////////////////////////////////////////////////////////////////////////////////////////
	View::View() = default;

	////////////////////////////////////////////////////////////////////////////////////////////////
	View::~View() = default;

	////////////////////////////////////////////////////////////////////////////////////////////////
	void View::RaiseEvent(const Event& event)
	{
		auto& eventDispatcher = GetDependency<EventDispatcher>();
		eventDispatcher.DispatchEvent(event);
	}
}
