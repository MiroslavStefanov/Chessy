#include "pch.h"
#include "ViewId.h"

namespace mvc
{
	////////////////////////////////////////////////////////////////////////////////////////////////
	ViewId ViewId::Invalid()
	{
		const int INVALID_VIEW_ID = -1;
		return ViewId(INVALID_VIEW_ID);
	}

	////////////////////////////////////////////////////////////////////////////////////////////////
	ViewId::ViewId(int id) : m_value(id)
	{
	}

	////////////////////////////////////////////////////////////////////////////////////////////////
	bool ViewId::IsValid() const
	{
		return (*this) != Invalid();
	}

	////////////////////////////////////////////////////////////////////////////////////////////////
	bool ViewId::operator==(const ViewId& other) const
	{
		return m_value == other.m_value;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////
	bool ViewId::operator!=(const ViewId& other) const
	{
		return !((*this) == other);
	}

	////////////////////////////////////////////////////////////////////////////////////////////////
	ViewId& ViewId::operator=(const ViewId& other)
	{
		m_value = other.m_value;
		return *this;
	}

	////////////////////////////////////////////////////////////////////////////////////////////////
	ViewId::operator int() const
	{
		return m_value;
	}
}